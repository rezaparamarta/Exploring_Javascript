// Buat looping untuk angkot dan ojeg
// Untuk angkot buat 10 looping dan mencetak "Angkot N0 sesuai looping beroperasi dengan baik" secara inceremental
// dan untuk ojeg secara decremental dari 100;
// Increment loop
let angkot = 1;
while (angkot <= 10) {
    console.log('Angkot No. ' + angkot + ' beroperasi dengan baik');
    angkot++;
}
// Decrement loop
let ojeg = 100;
while (ojeg >= 1) {
    console.log('Ojeg No. ' + ojeg + ' beroperasi dengan baik')
    ojeg--;
}

console.log('Angkot dan Ojeg selesai beroperasi hari ini.');